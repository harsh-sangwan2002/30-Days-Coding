num_1 = int(input("Enter the 1st number: "))
num_2 = int(input("Enter the 2nd number: "))

# Addition
res = num_1 + num_2
print(res)

# Subtraction
res = num_2 - num_1
print(res)

# Multiplication
res = num_1 * num_2
print(res)

# Division
res = num_2 // num_1
print(res)

# Modulus
res = num_2 % num_1
print(res)